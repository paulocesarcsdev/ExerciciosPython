"Faça um Programa que peça as 4 notas bimestrais e mostre a média."
nota_um = int(input("Entre com a nota um: "))
nota_dois = int(input("Entre com a nota dois: "))
nota_tres = int(input("Entre com a nota tres: "))
nota_quatro = int(input("Entre com a nota quatro: "))

media = (nota_um + nota_dois + nota_tres + nota_quatro) / 4

print("A média: ", media)
